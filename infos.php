<h2>Die Live-Band TS Rock</h2><br />
In diesem Jahr spielt zum vierten Mal die Live-Band <b>TS Rock</b> TS wird mit erstklassigen <b>Cover-Versionen</b> für <b>gute Laune</b> sorgen und aktuelle Titel aus <b>House</b> und <b>Rock-Klassikern</b>, aber auch <b>Party-Hits</b> und <b>deutsche Songs</b> zum Besten geben.<br />
Mehr über TS Rock erfahrt Ihr auf der offiziellen Internet-Seite <a href="http://www.ts-rock.de/band.html" target="blank">http://www.ts-rock.de</a>.
<br /><br />

<h2>Die verschiedenen Bars</h2><br />
Für ausreichend Getränke ist gesorgt, denn wir bieten Dir <b>3 verschiedene Bars</b>:<br />
<ul>
<li><b>Die Haupt-Bar</b>:<br />TS spielt live in der Halle. Hier darf deshalb auf keinen Fall eine Bar fehlen! Wir bieten Dir <b>verschiedenste Getränke</b>, für die Du die <b>Halle nicht verlassen</b> musst und direkt weiter feiern kannst!<br />&nbsp;</li>
<li><b>Die zweite Bar im Foyer</b>:<br />Sie befindet sich im <b>Vorraum zur Halle</b> (Foyer). Hier ist TS nicht zu hören, Du wirst vielmehr durch einen gelungen Mix aus <b>Disco-Musik</b> zum Feiern gebracht!<br />&nbsp;</li>
<li><b>Die Event-Bar oder Weizen-Bar</b>:<br />Du bist ein Freund von <b>exotischen und ungewöhlichen Drinks</b>? Dann ist die Event-Bar, die sich ebenfalls in der <b>Halle</b> befindet, genau das richtige für Dich!</li>
</ul>
In allen Bars werden unter Anderem auch alkoholische Getränke verkauft. Es sollte selbstverständlich sein, dass wir <b>Getränke ab 18</b> Jahren auch <b>nur an mindestens 18-jährige</b> verkaufen!<br /><br />Wenn Du <b>zwischen 16 und 18</b> Jahre alt bist, beschränkst Du Dich bitte auf Getränke, die <b>Bier, Wein oder Sekt</b> enthalten und verzichtest auf Hochprozentiges!
<br /><br />

<h2>Alterskontrolle</h2><br />
Die Erfahrung hat leider gezeigt, dass in den letzten Jahren zum Teil Gäste unter 14 Jahren anwesend waren.<br /><br />
Um diesem Trend entgegen zu wirken, werden wir ab diesem Jahr nur noch Gäste hereinlassen, die <b>mindestens 16 Jahre</b> alt sind!<br /><br />
Ihr kennt das ja bereits von anderen Parties, z.B. bei den KS-Parties im Nightlife. Wir werden im Gegensatz zum Centro allerdings <b>nicht</b> Eure Ausweise <b>einsammeln</b>. Wir vertrauen auf Euch, dass <b>Gäste unter 18 Jahren</b> spätestens um <b>0 Uhr nach Hause</b> gehen!<br /><br />
Wir wollen mit dieser Regelung auch eine angenehmere Atmosphäre für Euch schaffen, habt also bitte Verständnis dafür.<br /><br />
Weitere Informationen zum Jugendschutz findest Du beim <a href="http://www.bmfsfj.de/BMFSFJ/Kinder-und-Jugend/kinder-und-jugendschutz.html" target="blank">Bundesministerium für Familie, Senioren, Frauen und Jugend</a>.
<br /><br />

