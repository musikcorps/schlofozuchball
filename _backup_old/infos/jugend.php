<h3>Wichtige Informationen zum Jugendschutz</h3>

<p>Für den Einlass gilt:
<ul>
<li>unter 16 Jahren nur in Begleitung einer erziehungsbeauftragen Person</li>
<li>Jugendliche zwischen 16 und 18 Jahren müssen die Veranstaltung spätestens um 24 Uhr verlassen, wenn keine erziehungsbeauftrage Person dabei ist</li>
</ul>

<p>Wenn Du noch keine 18 Jahre alt bist und die Möglichkeit hast mit einer erziehungsberechtigten Person zum Schlofozuchball zu kommen, dann 
fülle unbedingt das von uns <a href="http://www.schlofozuchball.de/infos/Erziehungsbeauftragung.pdf">zur Verfügung gestellte Formular</a> aus, 
lass es von den betroffenen Personen unterschreiben und bring es mit.</p>

<p>Für den Getränkeausschank gilt:
<ul>
<li>Jugendliche unter 16 Jahre erhalten keine alkoholischen Getränke</li>
<li>Jugendliche zwischen 16 und 18 Jahre dürfen Bier kaufen (kein Rigo, kein Smirnoff und nichts Hochprozentiges aus der Bar)</li>
</ul>

